    function analyzeTone(msg) {
        const lower = msg.toLowerCase();
        if (/злой|злость|бешен|ярост|убью|уничтож|ненавиж|терпеть|пошёл|нахуй|идиот|дурак|тупой|козел|сволочь|бля|пизд|хуйня|пидар|гандон|мудак|падла|тварь|сука|шлюха|мразь|гад|сволочь/i.test(lower)) return 'aggressive';
        if (/❤️|💕|крут|класс|отличн|супер|лучш|обожаю|люблю|кайф|топ|клёв|прекрасн|шикарн|замечательн|хорош|рад|счасть|весел|улыб|😊|😁|😍|🥰|👍|🔥|💯/.test(lower)) return 'positive';
        if (/плох|ужасн|отвратит|кошмар|не нрав|бесит|надоел|достал|раздража|гадк|ненавиж|😠|👎|💔|😡|🤬/.test(lower)) return 'negative';
        if (/😉|😏|😜|🤪|😘|💋|хихи|шалун|заигрыв|флирт|подкат|соблазн|кокет|игрив/.test(lower)) return 'playful';
        if (/😢|😭|😔|😞|😟|груст|печаль|тоск|плак|жалк|несчаст|депресс|одинок/.test(lower)) return 'sad';
        return 'neutral';
    }

    function getMirrorResponse(msg) {
        const lower = msg.toLowerCase().trim();
        const homophobicPatterns = ['ты пидор', 'ты голубой', 'ты педик', 'ты гомик', 'ты лесби', 'ты нетрадиц', 'ты ориентац', 'ty gey', 'ty pidr'];
        if (homophobicPatterns.some(p => lower.includes(p))) {
            return randomItem([
                "Сам такой", "А у тебя с этим проблемы?", "Не, ну сам-то?",
                "Тебя это так волнует?", "Зачем тебе это знать?", "А ты интересуешься?",
                "Не твоё дело", "О себе расскажи", "Сам гей, раз так интересуешься",
                "Тебе завидно?", "Ты бы определился сначала", "А ты сам кто?",
                "Не проецируй", "Ты это к чему?", "В XXI веке это всё ещё актуально?",
                "Ориентация не повод для шуток", "Лучше бы о деле спросил",
                "Меньше думай о чужой ориентации", "Тебе лишь бы спросить",
                "А сам не хочешь признаться?"
            ]);
        }
        const match = lower.match(/^(ты|ти)\s+(\w+)/i);
        if (match) {
            const word = match[2];
            const neutralWords = ['кто', 'что', 'как', 'где', 'когда', 'зачем', 'почему', 'откуда'];
            if (neutralWords.includes(word)) return null;
            const template = randomItem(mirrorResponses);
            return template.replace('{word}', word);
        }
        return null;
    }


    function isYesNoQuestion(msg) {
        const lower = msg.toLowerCase().trim();
        if (!msg.includes('?')) return false;
        if (lower === '?' || lower.length < 3) return false;
        const yesNoIndicators = ['ли', 'да', 'нет', 'можно', 'стоит', 'нужно', 'будет', 'ты', 'вы', 'он', 'она', 'они', 'это', 'этот', 'пойдёшь', 'купишь', 'сделаешь', 'придёшь', 'позвонишь', 'хочешь', 'любишь', 'знаешь', 'понимаешь', 'видел', 'есть', 'был', 'будет', 'бывает', 'является'];
        const openQuestionWords = ['кто', 'что', 'где', 'когда', 'почему', 'зачем', 'откуда', 'как', 'какой', 'какая', 'какое', 'какие', 'сколько', 'чей'];
        for (let w of openQuestionWords) if (lower.includes(w)) return false;
        return yesNoIndicators.some(ind => lower.includes(ind));
    }

